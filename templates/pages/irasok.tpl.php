<pre>Rövid ismertető a játékról:</pre>
				<p>A S.T.A.L.K.E.R.: Shadow of Chernobyl egy nyílt világú, belső nézetű, lövöldözős játék, amelyet az ukrán GSC Game World fejlesztett és a THQ adott ki 2007 márciusában.
Néhány kifejezés a játékból („Zóna”, „Stalker”), mint ahogy néhány háttérsztori ötlet is a népszerű science-fiction könyvből, Arkagyij és Borisz Sztrugackij Piknik az árokparton című művéből ered, valamint az 1979-es Sztalker című film is a regényen alapszik. A zene egy része a játékban, mint például a rádióban felhangzó zene, vagy a Stalkerok gitárjátéka az ukrán FireLake metálzenekar által készített hangfelvétel.
A S.T.A.L.K.E.R.-ban a játékos egy „Stalker” szerepét ölti magára, egy illegális felfedezőt/artifact gyűjtőt a „Zónában”. A „Zóna” nem más, mint az alternatív valóságbeli verziója a csernobili erőműnek, annak második, fiktív felrobbanása után, amely a környező területeket sugárzással szennyezte be, és furcsa, e világtól elszakadt változásokat okozott a helyi flórában, faunában és még a fizika törvényeiben is. A „Stalker” szó az eredeti (film) kontextusában nagyjából „felfedező”-t jelent, vagy „vezető”-t, mivel a Stalker célja, hogy az embereket a Zónába elkísérje (vezesse).</p>

