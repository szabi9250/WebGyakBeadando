<h2>Fűrész</h2>
<p>Annak ellenére, hogy nem szerettem volna alapos bemutatót írni, a fűrészt természetesen ki kellett próbálnom. Az elődjével szerzett tapasztalatokra alapozva azt vártam, hogy jól fog működni, és alapvetően nem is kellett csalódnom. Noha érzésre egy kicsit kevésbé volt könnyű vele a munka, mint a Wave-vel, ezt a holtjáték számlájára írom.</p>

<h2>Csavarhúzó</h2>
<p>A Ganzo G302-H nagy előnye a Leathermanekkel szemben, hogy alapfelszereltségként kapunk mellé egy bitkészletet. Ezek ráadásul szabványos bitek, azaz olcsón szerezhetünk utánpótlást, vagy cserélhetjük ki a számunkra hasznosabb darabokra a készletben lévőket. Előzőleg ezt nagyon nagy pozitívumnak gondoltam, de most egy picit felülbíráltam saját véleményemet. No, nem a bitkészlet meglétével kapcsolatban, azt továbbra is jó ötletnek gondolom, ellenben a felszerelése egy kicsit macerás szerintem. A fogón lévő szárhoz kell csatlakoztatnunk egy mágneses foglalatot, majd abba kerülnek a bitek. Időnként jobban örültem volna, ha az alapbitek anélkül használhatóak lettek volna, hogy percekig kelljen mókolnom, de azért kivitelezhető a dolog.</p>

<h2>Egyebek</h2>
<p>Ehhez a szerszámhoz is kapunk néhány olyan funkciót, amelyek számomra nem fontosak, de lehet, hogy másoknak persze igen. Egyedül az ollót próbáltam ki, az teljesen jól működött nálam. Használható természetesen a konzervnyitó és a sörnyitó is, de a mondás szerint ugye minden sörnyitó, bár azért én is jobban preferálok egy célszerszámot.</p>

