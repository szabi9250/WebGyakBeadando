<h2>Fogó</h2>
<p>Nem lenne szükségünk semmi másra a svájci bicskákon kívül, ha lenne rajta fogó. A Ganzo G302-H ezen része remekül működik, sokkal jobban, mint fekete bevonatos társáé. Természetesen itt sem célszerszámról beszélünk, de apróbb feladatokra akár a csőrös része, akár a harapófogó, akár a drótvágó is bevethető. Utóbbival voltak problémáim a másik verziónál, de ezek itt nem jelentkeztek. Hasznos funkció lehet, hogy a drótvágó is cserélhető, így, ha elhasználódik, nem szükséges az egész szerszámot lecserélnünk.</p>

<h2>Tok</h2>
<p>A hordhatósággal kapcsolatban nem lehet okunk a panaszra, egy masszív, gyöngyvászonból készült tokot kapunk, amelyben a bitkészlet is megbújik. Horizontális és vertikális viselést egyaránt lehetővé tesz, a patentes zárás pedig megóvja eszközünket az elvesztéstől.</p>

