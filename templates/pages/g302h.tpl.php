<h2>Összegzés</h2>
<p>Nekem továbbra is vegyes érzéseim vannak a Ganzo multijaival kapcsolatban. Egyrészt nagyon olcsók, tehát mondhatjuk azt is, hogy elnézzük a hiányosságait. A fő problémám az, hogy ezek a hiányosságok sem előre megjósolhatók. Ahogy a gyártó késeinél, itt is ingadozik a minőség. Kifoghatunk egy tökéletes darabot is, de akár olyat is, amit nem fogunk szívesen használni, mint én a lötyögő szerszámok miatt ezt a példányt. (Ez a lötyögés elképzlehető, hogy orvosolható a tengelycsavar meghúzásával.)</p>
<p>Az Ganzo G302-H ára természetesen nagy fegyvertény, <a href="https://www.gearbest.com/multitools/pp_343441.html?lkid=19929868">tízezer forint</a> alatt hozzájuthatunk egy példányhoz, míg egy Leatherman Wave ennek közel ötszöröse. Ha valaki azért keres multit, hogy legyen egy a hátizsákban, amihez néha nyúlhat, vagy anyagi lehetőségei nem engednek meg nagyobb kiadást, bátran vehet egyet. Ha valaki azonban életre szóló darabot keres, az inkább spóroljon egy kicsit, és vegyen egy márkásabb darabot.</p>
