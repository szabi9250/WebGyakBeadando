<p>A Ganzo G302-H multifogó szerintem az utolsó állomása lesz az olcsó multikkal kapcsolatos próbálkozásaimnak. Annak idején egy noname, egy-két ezer forintos eszközzel kezdtem, aminek az első használatnál elhajlott a konzervnyitója, a másodiknál pedig lejöttek róla a markolatpanelek. Azóta volt már szerencsém ugyanennek a Ganzo fogónak a fekete változatához valamint egy kisebb testvéréhez, a Gerber Suspensionhoz, illetve más, olcsóbb multifogókhoz is, de eddig semmi nem tudta felülmúlni a Leatherman Wave-t. Vajon ennek sikerül?</p>

