<h2>Apróbb különbségek – Ganzo G302-H és G302-B</h2>
<p>Ez a bemutató a megszokottól eltérően nem lesz túlságosan alapos. Aki olvasta a <a href="?oldal=g302b">fekete verziórólkészült írásomat</a>, az emlékezhet rá, hogy alapvetően elégedett voltam az eszközzel, az árához képest egy korrekt darabról van szó. Az ezüstszínű változattól azt vártam, hogy kiküszöbölje azokat a problémákat, amelyeket annál tapasztaltam. Nos, ez részben sikerült is, másutt azonban sajnos visszalépést tapasztaltam. De nézzük egy picit részletesebben!</p>

<h2>Pengék</h2>
<p>Ha valaki nem hord magánál zsebkést, csak egy multit – van ilyen ember? -, annak fontos, hogy a szerszámon lévő vágóeszköz jól használható legyen. A Ganzo G302-H pengéjével két problémám van: az egyik a hossza, a másik pedig, hogy borzasztóan lötyög minden irányban. Ez a lötyögés olyannyira zavaró, hogy vágás közben is érzékelhető, tehát nem csak egy késsznob fanyalgását olvashatjátok ezzel kapcsolatban. Természetesen használható, és a zár sem adta meg magát vágás közben, de számomra ez nem volt bizalomgerjesztő.</p>
<p>A mérete még éppen rendben van szerintem, noha a Wave szerintem jól eltalált pengéjénél jó egy centivel rövidebb, inkább a Leatherman Juice késével vethető össze. Nyilván a fogószárak kialakítása volt itt a szűk keresztmetszet; mégis, néha egy centi is sokat számít, ha munkáról van szó.</p>
<p>Pozitívum viszont, hogy a fogazott penge a fekete bevonat nélkül remekül működött, a jelek szerint ott a dizájn ment a használhatóság rovására. Papírt és pántolószalagot is vágtam, és ha nem lett volna a mozgás, ami ennél a szerszámnál – ahogy az összes többinél – is jelentkezik, teljesen elégedett lettem volna.</p>

