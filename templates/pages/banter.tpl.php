<p>A markolatpanel mart barázdái kellemes hűvösséggel simulnak a kézbe, nagyon jó tapadást biztosítva. Hordás közben sajnálattal tapasztaltam, hogy hajlamos a kopásra, így akit ez zavar, gondolja meg ezen típusok vásárlását. A hossza is változott az első kiadáshoz képest. Mindössze 5 mm-el nőtt meg, de ez előnyévé válik a késnek. A lekerekítése pont annyira íves, hogy sokkal kényelmesebb használni, mint idősebb társát.</p>

<p>A két markolatpanel közti traktusban találhatóak a legnagyobb különbségek az első verzióhoz képest. A panelek alatt könnyített linerek vannak, immár a zár miatt mindkét oldalon.</p>
<p>A második a zár típusa. A WE is felült a keresztzár vonatra, ami manapság nagyon népszerű a késes körökben. Kijelenthetjük, hogy nemcsak meglovagolja ezt a sikerességet, hanem nevéhez méltóan szinte tökéletesre fejlesztette saját crossbar rendszerét. A piacon található egyik – ha nem a legjobban működő – keresztzárja ugyanis olyan szintű fidget faktorral rendelkezik, hogy az folyamatos játékra csábítja a gyanútlan késfelhasználót. Késbaráti társaságban körbeadva elképedő arcokat szült a Banter2, nem győztem felszedni a leesett állakat a földről. Mindenki szuperlatívuszokban beszélt róla, pedig a Benchmade szabadalmaztatta zártípus bemutatása óta jó pár gyártó saját verzióját kipróbálhatta a nagyérdemű. Ez viszont láthatóan nagyon betalált az érdeklődőknél.</p>

<p>A harmadik jól látható és hasznos különbség, hogy távtartók helyett egy, a markolattal színben harmonizáló backspacer került beépítésre. Szerepe leginkább a kés stabilizálása. Szerencsére a WE minőségre olyan jó példa, hogy nem igazán tűnik fel, annyira sikerült integrálni.</p>

