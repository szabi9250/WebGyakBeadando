<li> Molettázás</li>
<p>Sajnos erre a módosításra nem találtam  egy az egyben megfelelőt a magyar  nyelvben, így el kell fogadnotok az általam kitalált szót. A forma miatt  “Moletta”, alias Michele Pensato  jutott eszembe. Ő ugyanis előszeretettel alkalmaz ilyesfajta megjelenést az általa tervezett késeken. A penge alakja egy kisebb beavatkozásnak köszönhetően hosszban ugyan nem változott, de “molettásabb” kinézetet kapott. Tömzsibb megjelenésűvé vált, a hegy még kevésbé hajlamos a sérülésre, illetve a szúráskor fellépő ellenállás is talán kisebb a beavatkozás után.</p>
<li> Finis</li>
<p>A penge és a klipsz is kapott (már tőlem) egy házi kőmosást. A szatén pengefinis annyira nem az én világom, hogy a használat során keletkező harci sérülések minden egyes késemnél egy-egy gyomorszájra bevitt ütéssel érnek fel. Ezt megelőzve kőmostam és újra éleztem a Banter S35vn pengéjét. Számomra így már a megjelenés mellett a használat is első osztályúvá vált.</p>
</ol>

<h2>Milyen a Banter, ha a személyi száma kettes?</h2>
<p>Ben Petersen új Bantere bár kinézetre másképp mutat, alapvetően ugyanazt a szerepet hivatott betölteni, amit az első verzió. Egy mindenki számára kényelmesen hordható és használható, kiváló minőségű és megfizethető folder kíván lenni. A régi, faék egyszerűségű körvonal megmaradt ugyan, de már sokkal finomabb részletekkel kényezteti a késbarátokat.</p>
