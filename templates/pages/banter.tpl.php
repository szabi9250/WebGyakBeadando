<li> Furat</li>
<p>A show markolatpanelen elsőre feltűnik, hogy egy 8 mm-es furat fedi fel a távtartók közt megbúvó penge egy részét. Túl azon, hogy szerintem jól néz ki, van egy nagyon egyszerű gyakorlati haszna is. A zsebből kivéve ugyanis a G10 panel a szoros klipsszel együtt hajlamos volt nehézkessé tenni ezt az egyszerű mozdulatot. A furat ezt segít kiküszöbölni: a hüvelykujjam beleakasztva kényelmesen és csúszás nélkül tudom azonnal elővenni a modBantert a zsebemből. Mivel pár késnél találkoztam ezzel a megoldással, (Spyderco Watu, Fox Suru, Real Steel Bruns) örömmel fedeztem fel itt is.</p>
<li> Ujjvédő</li>
<p>A gyári verzióhoz képest az íves panelvég és alatta a penge lecsiszolásra került, így kb. egy centivel közelebb lehet fogni a penge tövéhez. Nekem ez volt az a módosítás, ami a legtöbbet tett azért, hogy nemcsak jól használható, hanem  állandó zseblakóvá vált a különc WE.</p>
<p> A gyári verzió ugyanis a vásárlást követő két hét után a szekrényben landolva pihenésre lett kárhoztatva. Nem bírtam megszokni, hogy az íves rész elvesz annyi hasznos helyet a markolatból, hogy ezáltal kényelmetlen, túlságosan rövid  számomra. A módosítást követően végre négyujjassá vált a kés, a finomabb munkákban könnyebben irányítható a penge, a fogása pedig egy cseppet sem lett veszélyesebb az ujjvédő hiánya miatt.</p>

