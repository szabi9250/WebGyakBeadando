<h2>We modBanter</h2>
<p>A customizált verzió az egyik késes csoport “Karácsonyi karika” elnevezésű játéka során került a tulajdonomba. Eredeti gazdája néhány olyan ponton módosított rajta a gyári változathoz képest, amik a felhasználói élményt nagyban képesek befolyásolni. Több éltárs is meglepődött, amikor kézbe fogta ezt az egyedi darabot. Bár majd mindenki találkozott az eredeti verzióval, az nem aratott akkora  sikert, mint ez a egyediség.</p>
<p>Nézzük sorban a módosításokat.</p>

<ol>
<li>Szín</li>
<p>Az eredetileg szürke markolatpanel egy speciális festékkel lila színűvé változott. Mélyen a G10 anyagába diffundálva garantálja, hogy nem fog kopni vagy fakulni a használat során a kés színe. Mindenhol egységes, átmenet és folt nélkül sikerült a művelet. Ha nem tudnám, hogy ilyen színben sohasem volt elérhető a Banter, akár gyári verziónak is hihetném.</p>
