<h2>Használat közben</h2>

<p>Sajnos sok újdonságot nem nagyon fogok tudni veletek megosztani. Amennyiben eltekintünk a penge 73 mm-es hosszától, egész jól használható a mindennapokban előforduló általános feladatokra. A relatív vékony penge teljes leélezése, pengegeometriája és a WE-től megszokott tökéletes élszalag kombinációja nagyon jó minőségű vágást tesz lehetővé. A szokásos konyhai munkák, a kartonvágás és étkezés után semmi nyoma a pengén a használatnak, ami jó jel.</p>
<p>Kézben fogva semmilyen kellemetlen nyomás vagy diszkomfort érzés nincs. Erősebb munkáknak még nem tettem ki, de az idő javulásával túrázásokkor is szándékomban áll bevetni majd.</p>

<h2>Konklúzió</h2>
<p>Hordása a deep carry klipsznek köszönhetően teljesen észrevétlenné teszi a Banter2-t.</p>
<p>Jó hír, hogy a penge fekete finise nálam eddig kopást még nem mutat, ellentétben az anodizált markolatú alumínium panellel. Könnyű és továbbra is szinte észrevétlen a zsebben, bár az új generáció már egyáltalán nem az a faék, aminek sokan tartották és tartják az előzőt. A kifogástalan zár és a felhasználói visszajelzések alapján megváltoztatott apróságok mind a vásárlók igényeinek kiszolgálását mutatja. Nyitása és csukása példaértékű, akár oktatóanyag is lehetne a késkészítő nagykönyvben. A penge semmilyen irányban nem kotyog vagy lötyög: teljesen stabilan áll, akár zárt, akár csukott állapotban van. Az általános minőségre nem nagyon lehet panasz, úgy vélem a pénzünkért egy kimondottan kellemes, immár sokkal kiforrottabb foldert kapunk, ami minden tekintetben felülmúlja elődjét. A Banter2 a nevéhez méltóan ugratja a versenytársakat, fricskát mutatva nekik az ár-érték-fidget faktor háromszögben.</p>
