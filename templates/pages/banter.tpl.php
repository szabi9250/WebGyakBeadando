<p>A markolatpanel klipsszel ellentétes oldalán egy, a pengével színben megegyező takarólemez vidítja fel a késes közösség balkezes tagjait. Ugyanis végre megfordítható lett a klipsz, így igazán kétkezessé vált Ben Petersen kése. Süllyesztett csavarjai nagy piros pontot érdemelnek, mondhatjuk: csak a szokásos WE minőség.</p>
<p>Az első generációhoz képest a logózott tengelycsavar nem olyan hangsúlyos, nekem jobban is tetszik.</p>
<p>A penge maradt a már jól bevált S35vn porkohászati acél, drop point formájú. Jelen esetben fekete kőmosottat kapunk, ami a lilával nagyon jó kontrasztot mutatva kellemes megjelenésűvé teszi alanyunkat. Továbbra is elmondható, hogy ez az acéltípus jól ellenáll a korróziónak, hosszan tartja az élét és házi körülmények közt is egész könnyen karbantartható. A rajta található jelzések megegyeznek az előző kiadásnál már megszokottakkal, úgymint csak a jobb oldalon látható diszkrét anyagjelölés és Ben Petersen jól ismert logója. Kerámia golyóscsapágyakon a penge a közhelyes “ zsírozott villám “ effektust bemutatva repül ki a helyéről, záráskor hasonlóan csukódik.</p>
<p>A rácsúszásgátló recézése kicsit durvább és a hossza pont a duplája a réginek, valamint a sharpening choil is megnőtt.</p>

