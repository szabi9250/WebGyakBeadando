<h2>Műszaki adatok:</h2>

<ul>
<li>Penge hossza: 73 mm </li>
<li>Penge anyaga: CPM S35VN </li>
<li>Penge vastagsága: 2,6 mm </li>
<li>Penge formája: Drop point </li>
<li>Markolat hossza: 96 mm </li>
<li>Markolat anyaga: Alumínium </li>
<li>Zár: Crossbar Lock</li>
</ul>

<p>A második generáció már nem különböző színű G10-es és fa markolatú panelekkel érhető el, hanem jelenleg micarta és anodizált, mart alupanelekkel. A három féle színhez 3 különböző pengefinis tartozik:</p>

<ul>
<li>Zöld micarta – kőmosott felület</li>
<li>Kék anodizált alumínium – szatén felület</li>
<li>Lila anodizált alumínium – fekete kőmosott felület</li>
</ul>
<p>A lila anodizált, mart alupanel nagyon jól harmonizál a fekete kőmosott felülettel. Hasonlóan feketére felületkezelték a crossbar lock nyitógombjait, a penge nyitószemölcseit, a klipszet és a csavarokat is.</p>
