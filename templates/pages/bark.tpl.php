<h2>Összegzés</h2>
<p>A Böker Bark Beetle egy átgondolt, jól használható kés, gyenge tokban. Ez a probléma természetesen orvosolható, akár ezt a tokot alakítjuk át, akár készíttetünk hozzá egy újat, persze ez jelentősen növelni fogja az amúgy kedvező árat.</p>
<p>És hogy végül az én késem lett-e a Szú? Nem, de nem azért, mert nem lenne jó választás akár egy Mora helyett: nem sokkal drágábban egy lényegesen szerethetőbb kést kapunk. De mivel tényleg nem én vagyok a célközönsége, nálam megszolgálta az árát: egy időre &#8211; legalábbis külsőségekben &#8211; én is túrázó és bushcrafter lehettem.</p>

