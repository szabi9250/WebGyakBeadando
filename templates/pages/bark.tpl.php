<h2>Közösségi háló</h2>
<p>Ezt az elméletemet tuti, hogy fejtegettem már itt a blogon, de sebaj, hátha valaki most olvas először. Gyanítom, hogy közösségi média nélkül jóval kevesebb kést vennénk, jóval átgondoltabban. Így viszont kétféle motivációnk is van az új beszerzésekre. Ha sokszor, sokaktól látunk valamit, azt esélyesebben kívánjuk meg. Másrészt, ha már megvan az adott dolog, akkor azt már mi is posztolhatjuk, és a lájkok mellett egyfajta közösséghez tartozás érzését is megkaphatjuk.</p>
<p>A Böker Bark Beetle nekem mindezeket hozza egy kicsit. Egyrészt magyar tervező munkája, amit egy világcég érdemesnek talált a sorozatgyártásra. Másrészt remekül néz ki: mind a formája, mind a micarta markolat kiváló fotóalannyá teszik. Na és egy ilyen kés birtoklásával már bekerülök az outdoor közösségbe is, nem?</p>

