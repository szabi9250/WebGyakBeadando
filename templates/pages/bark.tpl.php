<p>Öröm az ürömben, hogy mind a kipattogzást, mind pedig a foltokat az új tulajdonosa könnyen eltávolította a késről, a karbantarthatósága tehát ismét csak remek a késnek.</p>
<p>Ahogy Sándortól már megszokhattuk, az ergonómia is jól sikerült. A semleges forma számomra minden fogásnemben kényelmes volt, a micarta markolat pedig jó tapadást biztosít, így akár nedves kézzel is biztonságosan dolgozhatunk vele.</p>

<h2>Hordhatóság</h2>
<p>Ezen a téren sajnos kevésbé vagyok elégedett. A késhez mellékelt bőr tok méltatlanul gyenge. Sima erszénytokról van szó, ami ugyan tartja a kést, de mintha három-négy centivel hosszabb lenne a kelleténél, illetve maga a bőr is valamilyen olcsó megoldás, mert hamar elkezdett megkopni.</p>
<p>Az igazi feketeleves a felfogási módja. Egyfajta övre fűzős megoldást kapunk, ami az alsó felén patentos, így relatív könnyen fel- és leszerelhetjük. Ezzel a módszerrel számomra kényelmetlenül magasra esik a kés, és bár még használható, de hidegebb időben, sok ruha alól biztos, hogy macerás lenne előszedni. Kérdés még, hogy ez a patent mennyire tartós, el tudom képzelni, hogy a gyakorlatban, rendszeres használat mellett nem lenne hosszú életű.</p>
<p>Márpedig bármennyire jó egy kés, ha a tok a gyenge láncszem, az az egész csomagra jelentős hatást gyakorol. Természetesen ha valaki hajlandó a zsebébe nyúlni, egy egyedi bőr- vagy kydex tok csodákat képes művelni.</p>

