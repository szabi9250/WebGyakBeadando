<p>A Böker Bark Beetle késéről már akkor tudtam, hogy nem az én késem, amikor megrendeltem. Hogy miért vettem meg mégis? Nos, a nyilvánvaló okokat nem számítva azért, mert bíztam benne, hogy nem lesz igazam. Noha ritkán járok túrázni, így aztán bőven elvagyok akár egy alap Morával is, ez a kés lényegesen tetszetősebb annál. Ráadásul Hegyes Sándor tervezte, aki híres arról, hogy a kései átgondolt, remekül működő darabok. És mivel eredeti Hegyespengére továbbra sincs pénzem, a Böker lehetőséget adott arra, hogy mégis kipróbáljam a Bark Beetle-t. A kérdés csak az, hogy a próbaidőszak végére sikerült-e megváltoztatnom a véleményemet. Kicsit csalok, a következőkben ugyanis egy késbemutatónak álcázott filozofálgatás következik. Remélem, nem bánjátok.</p>
