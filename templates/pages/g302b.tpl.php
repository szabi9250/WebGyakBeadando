<h2>Első benyomások</h2>

<p>A Ganzo G302-B multiszerszám egy vastag, fekete kartondobozban érkezik, és már kibontás előtt az az érzésünk támad, hogy súlyos, masszív darabról van szó. A dobozt kinyitva először a gyöngyvászon övtokkal találkozunk, amelyen csupán a patenten láthatjuk a Ganzo logóját. Ezen belül rejtőzik maga a multiszerszám, illetve a hozzá járó bitkészlet egy külön kis zsebben a tokon belül. A dizájn egyébként itt is ismerős lehet, nekem elsőre a Gerber Suspension fogója jutott eszembe róla. A szerszám meglehetősen tetszetős, nekem kifejezetten bejön a fekete szín, külön piros pontot érdemel a Ganzo logo elhelyezése a fogó szárain.</p>

