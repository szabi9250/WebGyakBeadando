<p>Az olló szintén kellemes csalódás volt. Aprócska, rugós kialakításának köszönhetően jól működik, az éle is rendben volt.</p>
<p>Különlegesség a Ganzo szerszámjánál, hogy nem beépített csavarhúzót kapunk, hanem egy mágneses adaptert, amellyel szabványos biteket használhatunk. A G302-B-hez alapból jár egy 11 elemből álló bitkészlet, amelyet egy gumis tárolóval a szerszám övtokjában helyezhetünk el. Ezek minőségéről nem tudok még érdemben nyilatkozni, egyszerűbb feladatoknál jól működtek. Csukott állapotban történő használatakor a fogó ergonomikus kialakítása miatt egy kicsit furcsán lehet vele dolgozni, de ha kinyitjuk a fogót, ez a probléma megoldódik, legalábbis nekem így sokkal jobban kézre állt.</p>
<p>A többi szerszámot nem próbáltam ki egyelőre, feltételezem, hogy egy konzervnyitót vagy egy sörnyitót nem lehet elrontani. Ami nekem egy kicsit furcsa, az a kis késpengével kombinált ár. Ez sajnos szintén életlenre sikerült, ráadásul én az árat egy sokkal hegyesebb valaminek képzelem el.</p>
<p>A tesztüzem során a külső szerszámokat tartó zár stabilnak bizonyult, nem volt vele problémám. Szimpatikus volt az a megoldás is, hogy csukott állapotban is látjuk, hogy épp melyik pengéhez férünk hozzá, szerintem ez egy ötletes dolog.</p>

