<p>További probléma volt, hogy ha nagyobb erőt akartam kifejteni vele, a túl nagyra nyíló fogókarok miatt hátrébb kellett ráfognom a szokásosnál, és ilyen esetekben a csúszásgátló recézések eléggé nyomták a tenyeremet, kényelmetlenek voltak. Ráadásul a laza külső szerszámok – amik ilyenkor a belső felére kerülnek a markolatnak – itt is problémásak voltak, mert amikor keményebb anyagot vágtam, és hirtelen csukódott össze a fogó, a markolat belseje felé kinyíltak. Lehet, hogy tovább kell még húzni a tengelycsavarokat, bár félő, hogy az meg a nyitást fogja nehezíteni. Finomabb vágásoknál azonban jól működött mind a kicsit lekerekített forma, mind a már említett recézés.</p>

