<h2>Használat közben</h2>

<p>Ahogy fentebb már említettem, egy Leatherman Wave-et hordok magammal immár évek óta napi szinten. Ez persze nem azt jelenti, hogy állandóan használatban lenne, de időnként azért jó szolgálatot tett. Ilyen hosszú távú tapasztalataim a Ganzoval természetesen nincsenek, de megpróbáltam vele megoldani egy-két dolgot, amiről tudtam, hogy amerikai felmenőjének nem okozhatna gondot.</p>
<p>A tesztet a késpengével kezdtem, noha zsebkés szinte mindig van nálam a fogóval párhuzamosan. Az általam tesztelt két Ganzo folder után kissé meglepett, hogy a késpenge nem volt igazán éles, a fénymásolópapírt inkább tépte, mint vágta, és a kartonpapírral is meggyűlt a gondja. Nem volt használhatatlan, de azt sem mondanám, hogy öröm volt vele a munka. Ez a probléma szerencsére egy pár perces élezés után megoldódott, innentől kezdve a sima penge már kitűnően tette a dolgát. Ugyan én egy kicsit rövidnek találtam, de másodlagos vágószerszámnak megteszi.</p>

