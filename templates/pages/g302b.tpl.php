<p>A fogazott penge ugyanakkor gyári állapotban az én példányomon teljesen használhatatlan volt, mintha elfelejtették volna megélezni, vagy élezés után festették volna le, ami a vágóképességének nem tett jót. Ez konkrétan annyit jelent, hogy nem vágott semmit: sem papírt, sem zsinórt, sem pántolószalagot. Egy kicsit dolgoztam vele a Spyderco Sharpmakerrel, amitől javult ugyan, de nem lett az igazi. Szerintem kellő időbefektetéssel használható állapotba lehet hozni, de eredeti állapotában pont annyit ér, mintha rá sem tették volna.</p>

<p>Nálam a kábelcsupaszító szintén nem működött, életlen volt az is, szerencsére a késpengével ezt a feladatot egyébként is meg lehet oldani.</p>
<p>Számomra meglepően jól muzsikált viszont a fűrész. Itt is jobb lenne egy hosszabb méret, de így is lehet vele dolgozni, szépen, egyenletesen vág.</p>

