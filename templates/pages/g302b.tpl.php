<p>Kézbe véve az ergonómia nekem egy kicsit furcsa volt, de ezt az is okozhatja, hogy évek óta egy Leatherman Wave-et használok, ugyanakkor ismét elfogott az az érzés, hogy egy igazán masszív, minőségi darabot tartok a kezemben, nincs az a gagyi hatása a szerszámnak, mint a nagyáruházakban kapható filléres utánzatoknak. Ami nekem kifejezetten tetszett, az az, hogy a szerszámok a fogó csukott állapotában érhetők el, és kinyitásuk után mindegyik rögzül. A rögzítés egyébként a markolat két oldalán lévő, recézett csúszkával oldható, akár egy ujjal is, bár kettővel azért kényelmesebb. Ami már kevésbé volt tetszetős a szerszámokkal kapcsolatban, hogy némelyiket kifejezetten nehéz nyitni, főképp, ha az embernek rövidek vagy gyengék a körmei, illetve, hogy a szerszámok oldalirányban eléggé lötyögősek voltak, ezt szerencsére a tengelycsavarok megszorítása orvosolta.</p>
<p>A szerszámot kinyitva jutunk el magához a fogóhoz. Itt még szembetűnőbb az ívelt markolatkialakítás, meglehetősen jó fogást biztosít. A fogó egyébként rugós, tehát könnyedén használható egy kézzel, viszont a kínai tervezők valami miatt szabadon hagyták ezt a rugót, ami egyrészt nem túl szép, másrészt sérülékeny is lehet.</p>

