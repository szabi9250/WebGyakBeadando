<p>A kívülre helyezett funkciók után nem maradt más hátra, mint kipróbálni a fogó részt, elvégre ettől válik különlegessé egy multiszerszám; a többi funkciót egy svájci bicskán is megtalálhatjuk. Ugyanakkor tisztában kell lennünk azzal, hogy ezek a fogók még a márkás multik esetében sem tudnak helyettesíteni egy célszerszámot, a kompakt kivitel óhatatlanul azzal jár, hogy a fogónk gyengül, igazán durva munkára tehát vélhetőleg nem lesz alkalmas.</p>
<p>A fogófej alapvetően hasonlít a Leatherman Wave-éhez. Kapunk egy félkerekcsőrű fogót és egy harapófogót, illetve drótvágót, ami itt ráadásul cserélhető is. A fogó rész jól működik, kicsit feszegettem is vele, amit jól tolerált. Aztán sort kerítettem a drótvágási tesztre, és sajnos kissé csalódnom kellett. Közelebbről szemrevételezve már elsőre feltűnhet, hogy a drótvágó nem igazán éles. Többféle típusú drótot próbáltam darabolni vele, és kontrollcsoport gyanánt bevetettem a Wave-et is. Azt tapasztaltam, hogy egyes darabokkal megbirkózik a Ganzo, így pl. egy szabványos kerítésdrót nem okozott neki gondot, máskor viszont elbukik. Megtörtént ez túl erős és túl gyenge anyagnál is, így aztán az az érzésem, hogy inkább funkcionál dróttörőként, mint tényleges drótvágóként. Azt nem tudom, hogy csak az én példányom ilyen-e, illetve, hogy a cserélhető vágóélek másképp működnek-e, de itt sajnos nem éreztem elég meggyőzőnek a G302-B-t.</p>

